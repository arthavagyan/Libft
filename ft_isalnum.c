#include "libft.h"

int	ft_isalnum(int symb)
{
	if ((symb >= 65 && symb <= 90) || (symb >= 97 && symb <= 122))
		return (1);
	else if (symb >= 48 && symb <= 57)
		return (1);
	return (0);
}
